// -----------------------------------------------------------------------------
// Helpers
// -----------------------------------------------------------------------------

export function getByCategory(category: Category): DesignProject[] {
  return CATALOG.filter((p) => p.category === category);
}

export function getById(id: string): DesignProject | undefined {
  return CATALOG.find((p) => p.id === id);
}

export function search(query: string): DesignProject[] {
  const q = query.toLowerCase();
  return CATALOG.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q) ||
      p.stack.some((s) => s.toLowerCase().includes(q)) ||
      p.highlights.some((h) => h.toLowerCase().includes(q))
  );
}

export function filterByElement(element: DesignElement): DesignProject[] {
  return CATALOG.filter((p) => p.designElements.includes(element));
}

export function filterByDifficulty(difficulty: Difficulty): DesignProject[] {
  return CATALOG.filter((p) => p.difficulty === difficulty);
}

export function getAllExtractableComponents(): ExtractableComponent[] {
  return CATALOG.flatMap((p) => p.extractableComponents);
}

export function getStats() {
  return {
    totalProjects: CATALOG.length,
    totalStars: CATALOG.reduce((sum, p) => sum + p.stars, 0),
    totalComponents: CATALOG.reduce((sum, p) => sum + p.extractableComponents.length, 0),
    categories: Object.keys(CATEGORIES).length,
    byDifficulty: {
      principiante: CATALOG.filter((p) => p.difficulty === 'principiante').length,
      intermedia: CATALOG.filter((p) => p.difficulty === 'intermedia').length,
      avanzada: CATALOG.filter((p) => p.difficulty === 'avanzada').length,
    },
  };
}
